package com.dashboard.beans;

public class EmployeeDashboardBean {

	private CompensationDetailsBean compensationDetails;
	private InsuranceDetailsBean insuranceDetailsBean;
	private PersonalDetailsBean personalDetailsBean;
	private RoleDetailsBean roleDetailsBean;
	private SkillDetailsBean skillDetailsBean;
	private UnitDetailsBean unitDetailsBean;
	
	public CompensationDetailsBean getCompensationDetails() {
		return compensationDetails;
	}
	public void setCompensationDetails(CompensationDetailsBean compensationDetails) {
		this.compensationDetails = compensationDetails;
	}
	public InsuranceDetailsBean getInsuranceDetailsBean() {
		return insuranceDetailsBean;
	}
	public void setInsuranceDetailsBean(InsuranceDetailsBean insuranceDetailsBean) {
		this.insuranceDetailsBean = insuranceDetailsBean;
	}
	public PersonalDetailsBean getPersonalDetailsBean() {
		return personalDetailsBean;
	}
	public void setPersonalDetailsBean(PersonalDetailsBean personalDetailsBean) {
		this.personalDetailsBean = personalDetailsBean;
	}
	public RoleDetailsBean getRoleDetailsBean() {
		return roleDetailsBean;
	}
	public void setRoleDetailsBean(RoleDetailsBean roleDetailsBean) {
		this.roleDetailsBean = roleDetailsBean;
	}
	public SkillDetailsBean getSkillDetailsBean() {
		return skillDetailsBean;
	}
	public void setSkillDetailsBean(SkillDetailsBean skillDetailsBean) {
		this.skillDetailsBean = skillDetailsBean;
	}
	public UnitDetailsBean getUnitDetailsBean() {
		return unitDetailsBean;
	}
	public void setUnitDetailsBean(UnitDetailsBean unitDetailsBean) {
		this.unitDetailsBean = unitDetailsBean;
	}
	
	
}
